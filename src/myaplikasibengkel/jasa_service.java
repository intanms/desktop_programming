    public void loadTabelJasa(){
        String sql = "SELECT * FROM data_service";
        Object[] kolom = {"ID", "Nama Jasa", "Harga"};
        DefaultTableModel dataModel = new DefaultTableModel(null, kolom);
        tbJasa.setModel(dataModel);
        tbJasa.getColumnModel().getColumn(0).setMaxWidth(30);
        
        try{
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while(rs.next()){               
                int id = rs.getInt("id_service");
                String nama = rs.getString("nm_services");
                String harga = rs.getString("harga");
              
            
                Object[] data={id, nama, harga};
                dataModel.addRow(data);
            }
        } catch (SQLException e){
            System.out.println(e.getMessage());
        }
    }

    public void loadTabelJasa(String teks){
        String sql = "SELECT * FROM data_service WHERE nm_services like '%" + teks + "%'";
        Object[] kolom = {"ID", "Nama Jasa", "Harga"};
        DefaultTableModel dataModel = new DefaultTableModel(null, kolom);
        tbJasa.setModel(dataModel);
        tbJasa.getColumnModel().getColumn(0).setMaxWidth(30);
        
        try{
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while(rs.next()){
                int id = rs.getInt("id_service");
                String nama = rs.getString("nm_services");
                String harga = rs.getString("harga");
          
                
                Object[] data={id, nama, harga};
                dataModel.addRow(data);
            }
        } catch (SQLException e){
            System.out.println(e.getMessage());
        }
        
    }
