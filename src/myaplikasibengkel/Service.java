    private void cariData() {
        try {
            Connection conn = Koneksi.connectDB();
            String sql = "SELECT * FROM isi WHERE no_services = ? and id_service = ? ";    
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, lbNoServis.getText());
            st.setString(2, id_service);
            ResultSet rs = st.executeQuery();
        if (rs.next()) {
            txtJumlah.setText(rs.getString("jml_item"));
            
            if (rs.getString("discount").equals("0")){
            rb0.setSelected(true);
            }
            if (rs.getString("discount").equals("5")) {
            rb5.setSelected(true);
            }
            if (rs.getString("discount").equals("10")) {
            rb10.setSelected(true);
            }
            if (rs.getString("discount").equals("15")) {
            rb15.setSelected(true);
            }
            inisialisasi();
            
        }else{
            inisialisasi();
            btnTambah.setEnabled(true);
            txtJumlah.setText("");
            rb0.setSelected(true);
        }
        conn.close();
        st.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
    
