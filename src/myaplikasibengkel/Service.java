    private void txtJumlahKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumlahKeyPressed
        // TODO add your handling code here:
        String jumlah = txtJumlah.getText();
        int l = jumlah.length();
        
         if ( (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') && l < 4 ||evt.getKeyChar() == KeyEvent.VK_ENTER || evt.getKeyChar() == KeyEvent.VK_BACK_SPACE || evt.getKeyChar() == KeyEvent.VK_DELETE) {
                    txtJumlah.setEditable(true);
                } else {
                    txtJumlah.setEditable(false);
                    if ( l == 4 ) {
                    JOptionPane.showMessageDialog(this, "Jumlah tidak valid");
                    }else{
                    JOptionPane.showMessageDialog(this, "Inputan harus angka");
                    }
                }
    }//GEN-LAST:event_txtJumlahKeyPressed

